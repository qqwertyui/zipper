#include <gtest/gtest.h>
#include <string>
#include <vector>
#include "Zip.hpp"

bool test_archive(std::string name, std::vector<std::string> &expected) {
  Zip z(name);
  if(expected.size() != z.get_entries().size()) {
    return false;
  }
  bool ok = false;
  for(ZipEntry *e : z.get_entries()) {
    const std::string &current = e->get_name();
    for(std::string file : expected) {
      if(file.compare(current) == 0) {
        ok = true;
        break;
      }
    }
    if(ok == false) {
      printf("Failed at: %s\n", current);
      return false;
    }
  }
  return true;
}

TEST(UnzipArchive, unzip1) {
  std::string archive = "test1.zip";
  std::vector<std::string> expected = { 
    "cryptest_bds.bdsgroup", 
    "cryptest_bds.bdsproj", 
    "cryptest_bds.bpf", 
    "cryptlib_bds.bdsproj", 
    "cryptlib_bds.cpp" 
  };

  if(test_archive(archive, expected) == false) {
    FAIL();
  }
}


